class TestForceOnlyOverwritesDriftedFragment:
    """A drifted fragment (e.g. LLM-authored without the ID column) gets
    cleanly overwritten by the canonical pre-generator output when the
    skill calls `--force --only`."""

    def test_force_overwrites_assets_drift(self, tmp_path: Path):
        out = tmp_path / "out"
        out.mkdir()
        # Write a minimal yaml.
        import yaml as _y
        (out / "threat-model.yaml").write_text(_y.dump(_minimal_yaml()))

        # Plant a "drifted" assets.md (4-col, no ID column) like the LLM
        # historically did.
        frag = out / ".fragments"
        frag.mkdir()
        (frag / "assets.md").write_text(
            "## 4. Assets\n\n"
            "| Asset | Classification | Description | Linked Threats |\n"
            "|---|---|---|---|\n"
            "| Drift | Public | drift content | F-001 |\n",
            encoding="utf-8",
        )

        # Without --force, idempotency keeps the drifted version.
        rc = pregen.main([str(out), "--only", "assets.md"])
        assert rc == 0
        text = (frag / "assets.md").read_text()
        assert "drift content" in text  # drifted version preserved

        # With --force, canonical pre-generator output replaces it.
        rc = pregen.main([str(out), "--force", "--only", "assets.md"])
        assert rc == 0
        text = (frag / "assets.md").read_text()
        assert "drift content" not in text          # drift overwritten
        assert "| Asset | ID | Classification" in text  # ID column restored
        assert "A-001" in text                       # canonical IDs present

    def test_force_only_does_not_touch_other_fragments(self, tmp_path: Path):
        """`--force --only assets.md` must NOT regenerate other fragments
        even if they exist on disk and would otherwise be force-overwritten
        in a `--force` (no `--only`) run."""
        out = tmp_path / "out"
        out.mkdir()
        import yaml as _y
        (out / "threat-model.yaml").write_text(_y.dump(_minimal_yaml()))

        frag = out / ".fragments"
        frag.mkdir()
        # Custom system-overview that the skill should leave alone.
        (frag / "system-overview.md").write_text(
            "## 1. System Overview\n\nCUSTOM CONTENT — must survive --only assets.md.\n",
            encoding="utf-8",
        )
        rc = pregen.main([str(out), "--force", "--only", "assets.md"])
        assert rc == 0
        # system-overview is not touched.
        assert "CUSTOM CONTENT" in (frag / "system-overview.md").read_text()


