"""Tests for Patch P2 — Structural Determinism (A4 + A5 + A2).

Covers three orthogonal regressions:

    A4 — Mechanical structural fragments (`system-overview.md`,
         `architecture-diagrams.md`, `assets.md`, `attack-surface.md`,
         `out-of-scope.md`) are deterministically regenerated by the skill
         via `pregenerate_fragments.py --force --only ...`. The Phase 11
         LLM is no longer trusted to author these — any drift gets
         overwritten with the yaml-aligned canonical version.

    A5 — `gen_security_architecture(yaml_data, depth)` strips
         `<!-- NARRATIVE_PLACEHOLDER -->` comments from §7.4-§7.12 at
         quick depth so the LLM has no expansion bait there. §7.1, §7.2,
         §7.3 (with per-auth-method flow blocks), §7.13, §7.14 keep their
         placeholders since those are the high-value sections.

    A2 — `check_chain_tid_consistency` flags §3.1 chain-overview node
         labels that cite a T-NNN whose title in `threat-model.yaml`
         shares no content keyword with the label. Catches LLM-authored
         chain diagrams that reference completely the wrong threat.
"""

