class TestChainTidConsistency:
    """Verify the keyword-overlap heuristic catches mislabeled chain diagrams
    while tolerating paraphrase."""

    def _build_md_with_chain(self, label: str) -> str:
        return (
            "# Threat Model — Test\n\n"
            "## 3. Attack Walkthroughs\n\n"
            "### 3.1 Attack Chain Overview\n\n"
            "#### Chain 1 — Test\n\n"
            "```mermaid\n"
            "graph LR\n"
            f"    A[\"{label}\"] --> B[Outcome]\n"
            "```\n\n"
            "**Key takeaway:** Test.\n"
        )

    def _build_yaml(self, t_title: str) -> dict:
        return {
            "meta": {"schema_version": 1},
            "threats": [{"t_id": "T-001", "title": t_title}],
        }

    def test_flag_when_label_keywords_disjoint_from_title(self, tmp_path: Path):
        """The 2026-05-07 regression: chain says 'T-001 SQL injection login'
        but T-001 is the RSA-key finding. Must be flagged."""
        out = tmp_path / "out"
        out.mkdir()
        import yaml as _y
        (out / "threat-model.yaml").write_text(_y.dump(self._build_yaml(
            "Hardcoded RSA Private Key Enables Offline JWT Forgery"
        )))
        md = out / "threat-model.md"
        md.write_text(self._build_md_with_chain("T-001 SQL injection login endpoint"))
        report = qa.check_chain_tid_consistency(md, out)
        assert len(report.issues) == 1
        assert "T-001" in report.issues[0]
        assert report.ok == 0

    def test_pass_when_keywords_overlap_directly(self, tmp_path: Path):
        out = tmp_path / "out"
        out.mkdir()
        import yaml as _y
        (out / "threat-model.yaml").write_text(_y.dump(self._build_yaml(
            "Hardcoded RSA Private Key in lib/insecurity.ts"
        )))
        md = out / "threat-model.md"
        md.write_text(self._build_md_with_chain("T-001 RSA key in insecurity.ts"))
        report = qa.check_chain_tid_consistency(md, out)
        assert report.issues == []
        assert report.ok == 1

    def test_pass_with_morphological_variation(self, tmp_path: Path):
        """Real REF case: label says 'MD5 hashes cracked in seconds';
        title says 'MD5 password hashing crackable via rainbow tables'.
        The shared roots 'crack' / 'hash' should match via prefix logic."""
        out = tmp_path / "out"
        out.mkdir()
        import yaml as _y
        (out / "threat-model.yaml").write_text(_y.dump(self._build_yaml(
            "MD5 password hashing crackable via rainbow tables"
        )))
        md = out / "threat-model.md"
        md.write_text(self._build_md_with_chain("T-001 MD5 hashes cracked in seconds"))
        report = qa.check_chain_tid_consistency(md, out)
        assert report.issues == [], (
            f"morphological variation must not trip the check; got: "
            f"{report.issues}"
        )

    def test_skip_when_no_yaml(self, tmp_path: Path):
        """Legacy / pre-rendering: yaml absent → no-op pass."""
        out = tmp_path / "out"
        out.mkdir()
        md = out / "threat-model.md"
        md.write_text(self._build_md_with_chain("T-001 anything"))
        report = qa.check_chain_tid_consistency(md, out)
        assert report.issues == []
        assert report.ok == 1

    def test_skip_when_no_chain_overview(self, tmp_path: Path):
        """Quick-depth runs that omit §3.1 entirely → no-op pass."""
        out = tmp_path / "out"
        out.mkdir()
        import yaml as _y
        (out / "threat-model.yaml").write_text(_y.dump(self._build_yaml("anything")))
        md = out / "threat-model.md"
        md.write_text("# Threat Model\n\n## 1. Overview\n\nNo §3.1 here.\n")
        report = qa.check_chain_tid_consistency(md, out)
        assert report.issues == []
        assert report.ok == 1

    def test_skip_when_label_has_no_content_keywords(self, tmp_path: Path):
        """A bare 'T-001' label with no descriptive text can't be validated
        — the check intentionally skips rather than false-flagging."""
        out = tmp_path / "out"
        out.mkdir()
        import yaml as _y
        (out / "threat-model.yaml").write_text(_y.dump(self._build_yaml(
            "Hardcoded RSA Private Key in source"
        )))
        md = out / "threat-model.md"
        md.write_text(self._build_md_with_chain("T-001"))
        report = qa.check_chain_tid_consistency(md, out)
        assert report.issues == []
        assert report.ok == 1


class TestChainKeywordsOverlapHelper:
    """Unit-level checks on the prefix-matching logic."""

    def test_exact_match(self):
        assert qa._chain_keywords_overlap({"injection"}, {"injection"})

    def test_prefix_match(self):
        # "cracked" vs "crackable" share prefix "crack" (length 5).
        assert qa._chain_keywords_overlap({"cracked"}, {"crackable"})

    def test_substring_match(self):
        # "hash" ⊂ "hashing" — ≥ 4 chars on both sides.
        assert qa._chain_keywords_overlap({"hash"}, {"hashing"})

    def test_short_prefix_no_match(self):
        # "auth" (4 chars) vs "audit" (5 chars) share only 3 chars — no match
        # under the default min_prefix=5.
        assert not qa._chain_keywords_overlap({"auth"}, {"audit"})

    def test_no_overlap(self):
        assert not qa._chain_keywords_overlap(
            {"injection", "login"},
            {"forgery", "private", "rsa"},
        )


